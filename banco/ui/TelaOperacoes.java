    private void btn_confirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_confirmarActionPerformed
        String conta = edt_numeroConta.getText().trim();
        String valorStr = edt_valor.getText().trim();

        if (conta.isEmpty() || valorStr.isEmpty()) {
            lbl_warnings.setForeground(java.awt.Color.RED);
            lbl_warnings.setText("Por favor, preencha todos os campos obrigatórios!");
            return;
        }
        
        double valor;
        try {
            valor = Double.parseDouble(valorStr);
        } catch (NumberFormatException e) {
            lbl_warnings.setForeground(java.awt.Color.RED);
            lbl_warnings.setText("O valor digitado deve ser um número válido!");
            return;
        }

        banco.service.BancoService servico = new banco.service.BancoService();
        String mensagemResultado = "";

        if (this.operacaoDefinida.equals("SAQUE")) {
            banco.model.ContaBancaria cb = servico.buscarConta(conta);
            if (cb instanceof banco.model.ContaCorrente) {
                mensagemResultado = servico.sacarCorrente(conta, valor);
            } else {
                mensagemResultado = servico.sacarPoupanca(conta, valor);
            }
            
        } else if (this.operacaoDefinida.equals("DEPOSITO")) {
            banco.model.ContaBancaria cb = servico.buscarConta(conta);
            if (cb instanceof banco.model.ContaCorrente) {
                mensagemResultado = servico.depositarCorrente(conta, valor);
            } else {
                mensagemResultado = servico.depositarPoupanca(conta, valor);
            }
            
        } else if (this.operacaoDefinida.equals("TRANSFERENCIA")) {
            String contaDestino = edt_conta_destino.getText().trim();
            if (contaDestino.isEmpty()) {
                lbl_warnings.setForeground(java.awt.Color.RED);
                lbl_warnings.setText("Informe a conta de destino para a transferência!");
                return;
            }
            mensagemResultado = servico.transferir(conta, contaDestino, valor);
        }
        
        if (mensagemResultado.startsWith("Sucesso")) {
            lbl_warnings.setForeground(new java.awt.Color(0, 150, 0)); 
            edt_valor.setText(""); 
        } else {
            lbl_warnings.setForeground(java.awt.Color.RED);
        }
        
        lbl_warnings.setText(mensagemResultado);
    }//GEN-LAST:event_btn_confirmarActionPerformed

