    private void configurarTelaDinamica(){
        if (this.operacaoDefinida.equals("SAQUE")) {
            lbl_title.setText("Operação: Saque Bancário");
            btn_confirmar.setText("Confirmar Saque");
            lbl_conta_destino.setVisible(false);
            edt_conta_destino.setVisible(false);
        } 

        else if (this.operacaoDefinida.equals("DEPOSITO")) {
            lbl_title.setText("Operação: Depósito Bancário");
            btn_confirmar.setText("Confirmar Depósito");
            lbl_conta_destino.setVisible(false);
            edt_conta_destino.setVisible(false);
        }

        else if (this.operacaoDefinida.equals("TRANSFERENCIA")) {
            lbl_title.setText("Operação: Transferência (TED/Pix)");
            btn_confirmar.setText("Confirmar Transferência");
        }
    }

